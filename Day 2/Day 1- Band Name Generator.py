# DAY 1: BAND NAME GENERATOR PROGRAM

# Introduce the program
print("Welcome to the Band Name Generator, this program takes the name of the city you grew up and your pet's name to generate a band name for you")

# Request for city name
street = input("What's the name of the city you grew up in?\n")
# Request for pet name
pet = input("What's your pet's name?\n")

# Print out the band name for the user
print("Your band name could be " + street + " " + pet)